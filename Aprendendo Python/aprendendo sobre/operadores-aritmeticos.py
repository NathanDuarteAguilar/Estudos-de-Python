# Acrescentando espaços e exemplos de alinhamento
print("\nAcrescentando espaços e exemplos de alinhamento\n")
x = "Nathan"
print("Olá {:20}!".format(x))
print("Olá {:>20}!".format(x))
print("Olá {:<20}!".format(x))
print("Olá {:^20}!".format(x))
print("Olá {:=^20}!".format(x))

print('-------------------------------------------------------------------------------------------------------------------------------------')

