# Operadores aritméticos
# + adição
# - subtração
# * multiplicação
# / divisão
# ** potência, função interna de potência = pow(1,1)
# // divisão inteira
# % resto da divisão

print("Operadores aritméticos\n")
n1 = int(5)
n2 = int(5)
s = n1+n2
sub = n1-n2
m = n1*n2
d = n1/n2
di = n1//n2
e = n1**n2

print("A soma é {}, a subtração é {}, a multiplicação é {}, a divisão é {}, a divisão inteira é {} e a potência é {}".format(s,sub,m,d,di,e))

print('-------------------------------------------------------------------------------------------------------------------------------------')

