#Faça um programa em python que abra e reproduza o áudio de um arquivo mp3

from pygame import mixer

mixer.init()
mixer.music.load("ex021.mp3")
mixer.music.set_volume(0.7)
mixer.music.play()
while True:

    print("Digite 'p' para pausar ou 'r' para restartar'")
    print("Digite 'e' para encerrar o programa")
    music = input("  ")

    if music == 'p':
        mixer.music.pause()
    elif music == 'r':
        mixer.music.unpause()
    elif music == 'e':
        mixer.music.stop()
        break


